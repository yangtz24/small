/**
 * @ClassName: EurekaApplication
 * @Description: TODO
 * @author: yangtz
 * @date: 2020/10/14
 * @Version: V1.0
 */
@SpringBootApplication
@EnableEurekaServer
public class EurekaApplication {
    public static void main(String[] args) {
        SpringApplication.run(EurekaApplication.class, args);
    }
}
